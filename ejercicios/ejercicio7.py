# Lista de números reales
numeros = [12.5, 43.8, 9.1, 100.0, 56.7, 78.3, 21.4, 3.9]

# Mostrar la lista
print("Lista de números reales:")
print(numeros)

# Encontrar el valor máximo y mínimo
maximo = max(numeros)
minimo = min(numeros)

# Mostrar los resultados
print(f"\nEl número mayor es: {maximo}")
print(f"El número menor es: {minimo}")
