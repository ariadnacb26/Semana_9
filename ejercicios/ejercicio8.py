# Lista con valores repetidos
lista_con_duplicados = [2, 5, 3, 2, 8, 5, 9, 1, 3, 7]

# Crear una nueva lista sin duplicados usando set para eliminar repeticiones
lista_sin_duplicados = list(set(lista_con_duplicados))

# Mostrar las listas
print("Lista original con duplicados:")
print(lista_con_duplicados)

print("\nLista sin duplicados:")
print(lista_sin_duplicados)
